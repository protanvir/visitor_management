const roleBadgeColors: Record<string, string> = {
  admin: "badge-error",
  manager: "badge-success",
  user: "badge-primary",
};

export default function AuthenticatedLayout({ children }: { children: React.ReactNode }) {
  const { user, loading, isAuthenticated, logout } = useAuth();
  const router = useRouter();
  const pathname = usePathname();

  useEffect(() => {
    if (!loading && !isAuthenticated) {
      router.push("/login");
    }
  }, [loading, isAuthenticated, router]);

  if (loading) {
    return (
      <div className="min-h-screen bg-page flex items-center justify-center">
        <div className="loading w-8 h-8 text-brand"></div>
      </div>
    );
  }

  if (!isAuthenticated || !user) {
    return null;
  }

  const filteredNavItems = navItems.filter((item) => {
    if (item.href.startsWith("/admin")) return user.role === "admin";
    return ["admin", "manager", "user"].includes(user.role);
  });

  return (
    <div className="min-h-screen bg-page flex">
      {/* Sidebar */}
      <aside className="w-64 bg-surface border-r border-neutral-100 fixed left-0 top-0 h-full flex flex-col">
        {/* Logo */}
        <div className="flex items-center gap-3 px-4 py-4 border-b border-neutral-100">
          <div className="w-10 h-10 bg-brand text-white flex items-center justify-center rounded-lg font-bold text-lg">
            V
          </div>
          <div>
            <h1 className="font-bold text-heading">VMS</h1>
            <p className="text-xs text-muted">Visitor Management</p>
          </div>
        </div>

        {/* Navigation */}
        <nav className="flex-1 overflow-y-auto py-4">
          <ul className="space-y-1 px-3">
            {filteredNavItems.map((item) => {
              const isActive = pathname === item.href || pathname.startsWith(item.href + "/");
              return (
                <li key={item.href}>
                  <a
                    href={item.href}
                    className={`flex items-center gap-3 px-3 py-2.5 rounded-lg text-sm transition-colors ${
                      isActive
                        ? "bg-brand/10 text-brand font-medium"
                        : "text-muted hover:bg-neutral-50 hover:text-body"
                    }`}
                  >
                    <svg className="w-5 h-5" fill="none" stroke="currentColor" viewBox="0 0 24 24">
                      <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d={item.icon} />
                    </svg>
                    <span>{item.label}</span>
                  </a>
                </li>
              );
            })}
          </ul>
        </nav>

        {/* User section */}
        <div className="border-t border-neutral-100 p-4">
          <div className="flex items-center gap-3 mb-3">
            <div className="avatar w-10 h-10 text-sm">
              {user.name.charAt(0).toUpperCase()}
            </div>
            <div className="flex-1 min-w-0">
              <p className="text-sm font-medium text-heading truncate">{user.name}</p>
              <span className={`badge text-xs ${roleBadgeColors[user.role] || "badge-primary"}`}>
                {user.role}
              </span>
            </div>
          </div>

          <button
            onClick={logout}
            className="btn btn-ghost btn-sm w-full justify-start text-muted"
          >
            <svg className="w-4 h-4" fill="none" stroke="currentColor" viewBox="0 0 24 24">
              <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M17 16l4-4m0 0l-4-4m4 4H7m6 4v1a3 3 0 01-3 3H6a3 3 0 01-3-3V7a3 3 0 013-3h4a3 3 0 013 3v1" />
            </svg>
            Logout
          </button>
        </div>
      </aside>

      {/* Main content */}
      <main className="flex-1 ml-64">
        {children}
      </main>
    </div>
  );
}
