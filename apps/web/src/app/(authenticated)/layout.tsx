const roleBadgeColors: Record<string, string> = {
  admin: "badge badge-error",
  manager: "badge badge-success",
  user: "badge badge-primary",
};

export default function AuthenticatedLayout({ children }: { children: React.ReactNode }) {
  const { user, loading, isAuthenticated, logout } = useAuth();
  const router = useRouter();
  const pathname = usePathname();

  useEffect(() => {
    if (!loading && !isAuthenticated) {
      router.push("/login");
    }
  }, [loading, isAuthenticated, router]);

  if (loading) {
    return (
      <div className="min-h-screen flex items-center justify-center">
        <span className="loading loading-spinner loading-lg text-primary"></span>
      </div>
    );
  }

  if (!isAuthenticated || !user) {
    return null;
  }

  const filteredNavItems = navItems.filter((item) => item.roles.includes(user.role));

  return (
    <div className="min-h-screen flex">
      {/* Sidebar */}
      <aside className="w-64 min-h-screen bg-base-200 border-r border-base-300 fixed left-0 top-0 flex flex-col">
        {/* Logo */}
        <div className="flex items-center gap-3 px-4 py-4 border-b border-base-300">
          <div className="w-10 h-10 bg-primary text-primary-content flex items-center justify-center rounded-lg font-bold text-lg">
            V
          </div>
          <div>
            <h1 className="font-bold text-lg">VMS</h1>
            <p className="text-xs text-base-content/60">Visitor Management</p>
          </div>
        </div>

        {/* Navigation */}
        <nav className="flex-1 overflow-y-auto py-2">
          <ul className="menu px-2 gap-1">
            {filteredNavItems.map((item) => {
              const isActive = pathname === item.href || pathname.startsWith(item.href + "/");
              return (
                <li key={item.href}>
                  <a
                    href={item.href}
                    className={isActive ? "active" : ""}
                  >
                    {item.icon}
                    <span>{item.label}</span>
                  </a>
                </li>
              );
            })}
          </ul>
        </nav>

        {/* User section */}
        <div className="border-t border-base-300 p-4">
          <div className="flex items-center gap-3 mb-3">
            <div className="avatar placeholder">
              <div className="bg-primary text-primary-content w-10 h-10 rounded-full">
                <span className="text-sm">{user.name.charAt(0).toUpperCase()}</span>
              </div>
            </div>
            <div className="flex-1 min-w-0">
              <p className="text-sm font-bold truncate">{user.name}</p>
              <span className={`badge badge-sm ${roleBadgeColors[user.role] || "badge-primary"}`}>
                {user.role}
              </span>
            </div>
          </div>

          <div className="flex items-center justify-between mb-2">
            <span className="text-xs text-base-content/60">Theme</span>
            <ThemeSwitcher />
          </div>

          <button
            onClick={logout}
            className="btn btn-ghost btn-sm w-full justify-start"
          >
            <svg className="w-4 h-4" fill="none" stroke="currentColor" viewBox="0 0 24 24">
              <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M17 16l4-4m0 0l-4-4m4 4H7m6 4v1a3 3 0 01-3 3H6a3 3 0 01-3-3V7a3 3 0 013-3h4a3 3 0 013 3v1" />
            </svg>
            Logout
          </button>
        </div>
      </aside>

      {/* Main content */}
      <main className="flex-1 ml-64">
        {children}
      </main>
    </div>
  );
}
